    private void jButton9ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton9ActionPerformed
        // TODO add your handling code here:
        if(jTextField5.getText().equals(jTextField6.getText())){
            jTextField6.setText("");
            JOptionPane.showMessageDialog(null, "Not transfer in same account");
        }else{
            String sql="select * from ACCOUNTS where Acc_No=?";
            try{
                pst=conn.prepareStatement(sql);
                pst.setString(1, jTextField6.getText());
                rs=pst.executeQuery();
                if(rs.next()){
                    jTextField14.setText(rs.getString("Name"));
                    jTextField15.setText(rs.getString("Balance"));
                    rs.close();
                    pst.close();
                    jTextField6.setEditable(false);
                    jTextField7.setEditable(true);

                }
                else{
                    JOptionPane.showInternalMessageDialog(null, "Account Not Found");
                }
            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }finally{
                try{
                    rs.close();
                    pst.close();
                }catch(Exception e){
                    JOptionPane.showMessageDialog(null, e);
                }
            }
        }
    }//GEN-LAST:event_jButton9ActionPerformed


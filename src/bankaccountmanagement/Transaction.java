    public void credit(String acc,double curBal,double creditBal){
        curBal+=creditBal;
        String sql="update ACCOUNTS set Balance=? where Acc_No=?";
        try{
            pst=conn.prepareStatement(sql);
            pst.setString(1, String.valueOf(curBal));
            pst.setString(2, acc);
            pst.execute();
            rs.close();
            pst.close();
        }catch(Exception e){

            JOptionPane.showMessageDialog(rootPane, e);
        }       
    }
    public void debit(String acc,double curBal,double debitBal){
        curBal-=debitBal;
        String sql="update ACCOUNTS set Balance=? where Acc_No=?";
        try{
            pst=conn.prepareStatement(sql);
            pst.setString(1, String.valueOf(curBal));
            pst.setString(2, acc);
            pst.execute();
            rs.close();
            pst.close();
        }catch(Exception e){

            JOptionPane.showMessageDialog(rootPane, e);
        }     
    }
    public void Table(){
        try{
            String sql = "select Acc_No,Name,Acc_Type,Balance,Gender,Contact_No from ACCOUNTS";
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            jTable.setModel(DbUtils.resultSetToTableModel(rs));
            jTable.setEnabled(false);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }finally{
            try {
                rs.close();
                pst.close();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);                
            }
        }
    }
